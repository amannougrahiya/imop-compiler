package imop.lib.getter;

import imop.ast.node.external.*;
import imop.baseVisitor.DepthFirstVisitor;

import java.util.LinkedList;
import java.util.List;

/**
 * Populates the funcList with all the nodes of type FunctionDefinition
 */
public class AllFunctionDefinitionGetter extends DepthFirstVisitor {
	public List<FunctionDefinition> funcList = new LinkedList<>();

	/**
	 * f0 ::= ( DeclarationSpecifiers() )?
	 * f1 ::= Declarator()
	 * f2 ::= ( DeclarationList() )?
	 * f3 ::= CompoundStatement()
	 */
	@Override
	public void visit(FunctionDefinition n) {
		funcList.add(n);
	}
}
